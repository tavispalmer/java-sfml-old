package org.sfml_dev.system;

public class Vector2i {

    /** X coordinate of the vector */
    public int x;

    /** Y coordinate of the vector */
    public int y;

    public Vector2i() {
        this.x = 0;
        this.y = 0;
    }

    public Vector2i(int x, int y) {
        this.x = x;
        this.y = y;
    }
}